package day13;

public class maxAndMin {
    public static void main(String[] args) {
        int a=3;
        int b=5;
        if (a>b){
            System.out.println(a + " is max");
        }else{
            System.out.println(b + " is max");
        }
        if (a<b){
            System.out.println(a + " is min");
        }else{
            System.out.println(b + " is min");
        }
    }
}
